/**
 * useLocalStorage - Hook for persisting state to localStorage
 */

import { useState, useEffect, useCallback } from 'react';

export function useLocalStorage<T>(
  key: string,
  initialValue: T
): [T, (value: T | ((prev: T) => T)) => void] {
  // Get stored value or use initial
  const [storedValue, setStoredValue] = useState<T>(() => {
    if (typeof window === 'undefined') {
      return initialValue;
    }
    try {
      const item = window.localStorage.getItem(key);
      return item ? (JSON.parse(item) as T) : initialValue;
    } catch (error) {
      console.warn(`Error reading localStorage key "${key}":`, error);
      return initialValue;
    }
  });

  // Update localStorage when value changes
  useEffect(() => {
    if (typeof window === 'undefined') return;
    try {
      window.localStorage.setItem(key, JSON.stringify(storedValue));
    } catch (error) {
      console.warn(`Error setting localStorage key "${key}":`, error);
    }
  }, [key, storedValue]);

  // Setter that can take a value or updater function
  const setValue = useCallback((value: T | ((prev: T) => T)) => {
    setStoredValue((prev) => {
      const nextValue = value instanceof Function ? value(prev) : value;
      return nextValue;
    });
  }, []);

  return [storedValue, setValue];
}

// Storage keys for the app
export const STORAGE_KEYS = {
  lastPage: 'digitalkhatt-lastPage',
  layout: 'digitalkhatt-layout',
  zoom: 'digitalkhatt-zoom',
  tajweed: 'digitalkhatt-tajweed',
  verseFormat: 'digitalkhatt-verseFormat',
  sidebarOpen: 'digitalkhatt-sidebarOpen',
  spreadView: 'digitalkhatt-spreadView',
  engine: 'digitalkhatt-engine',
} as const;

export default useLocalStorage;
